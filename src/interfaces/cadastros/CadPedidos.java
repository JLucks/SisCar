     private boolean checarCampos(){
        String campo = jtfModelo.getText().replaceAll(" ", "");
        String campo2 = jtfAno.getText().replaceAll(" ", "");
        String campo3 = jtfQnt.getText().replaceAll(" ", "");
        int campo4 = jcbFilial.getSelectedIndex();
        int campo5 = jcbForn.getSelectedIndex();
        return !campo.equals("")&&!campo2.equals("")&&!campo3.equals("../-")&&!(campo4==0)&&!(campo5==0);
    }

    private void clearCampo(){
        jtfModelo.setText("");
        jtfAno.setText("");
        jtfQnt.setText("");
        jcbFilial.setSelectedIndex(0);
        jcbForn.setSelectedIndex(0);
    }
    
    private void loadFiliais(){
        List<Filial> list = daof.recuperaFilial();
        this.filiais = new String[list.size()+1];
        int i = 1;
        this.filiais[0] = "Selecione";
        for(Filial fil: list){
            this.filiais[i] = String.valueOf(fil.getMatricula());
            i++;
        }
    }
    
    private void loadFornecedor(){
        List<Fornecedor> list = daofor.recuperaFornecedor();
        this.fornecedores = new String[list.size()+1];
        int i = 1;
        this.fornecedores[0] = "Selecione";
        for(Fornecedor forn: list){
            this.fornecedores[i] = forn.getNome();
            i++;
        }
    }
    
    private String dateAlu(){
        String res, format = "yyyy/MM/dd";
        Calendar c = Calendar.getInstance();
        SimpleDateFormat date = new SimpleDateFormat(format);
        res = date.format(c.getTime());
        return res;
    }

