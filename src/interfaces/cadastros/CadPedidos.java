    private void bttSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttSalvarActionPerformed
        if(checarCampos()){
            Pedido ped = new Pedido();
            DAOpedido dao = new DAOpedido();
            ped.setData(dateAlu());
            ped.setModelo(jtfModelo.getText());
            ped.setAno(jtfAno.getText());
            ped.setQuantidade(Integer.parseInt(jtfQnt.getText()));
            ped.setIdFilial(daof.buscaFilial(jcbFilial.getSelectedItem().toString()).getCodFilial());
            ped.setIdFornecedor(daofor.recuperaFornecedor(jcbForn.getSelectedItem().toString()).getId());
            dao.adicionaPedido(ped);
            JOptionPane.showMessageDialog(null, "Realizado com sucesso!");
            clearCampo();
        }else{
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos!");
        }
    }//GEN-LAST:event_bttSalvarActionPerformed

