    private void loadElements(){
        List<Funcionario> list = dao.recuperaFuncionarios();
        this.objects = new String[list.size()][11];
        int i = 0;
        for(Funcionario fun: list){
            this.objects[i][0] = String.valueOf(fun.getCodFunc());
            this.objects[i][1] = fun.getNome();
            this.objects[i][2] = fun.getCpf();
            this.objects[i][3] = fun.getEndereco();
            this.objects[i][4] = fun.getTelefone();
            this.objects[i][5] = String.valueOf(fun.getGenero());
            this.objects[i][6] = fun.getNascimento();
            this.objects[i][7] = "RS "+ String.valueOf(fun.getSalario());
            this.objects[i][8] = fun.getAdmissao();
            this.objects[i][9] = String.valueOf(fun.getCargaH());
            this.objects[i][10] = String.valueOf(fun.getCodFilial());
            i++;
        }
    } 

