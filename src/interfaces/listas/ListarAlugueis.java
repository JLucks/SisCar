    private void loadElements(){
        List<Aluguel> list = dao.recuperaAlugueis();
        this.objects = new String[list.size()][10];
        int i = 0;
        for(Aluguel alu: list){
            this.objects[i][0] = String.valueOf(alu.getNumOrdem());
            this.objects[i][1] = String.valueOf(alu.getCodCli());
            this.objects[i][2] = String.valueOf(alu.getCodVen());
            this.objects[i][3] = String.valueOf(alu.getCodVei());
            this.objects[i][4] = alu.getDataAlu();
            this.objects[i][5] = alu.getDataDev();
            this.objects[i][6] = String.valueOf(alu.getPreco());
            this.objects[i][7] = String.valueOf(alu.getDesconto());
            this.objects[i][8] = String.valueOf(alu.getMulta());
            this.objects[i][9] = String.valueOf(alu.getTotal());
            i++;
        }
    } 

