     private void loadElements(){
        List<Filial> list = dao.recuperaFilial();
        this.filiais = new String[list.size()][3];
        int i = 0;
        for(Filial fil: list){
            this.filiais[i][0] = String.valueOf(fil.getCodFilial());
            this.filiais[i][1] = fil.getMatricula();
            this.filiais[i][2] = fil.getEndereco();
            i++;
        }
    } 

